package src.basics_03_017Dec;

public class Lab022 {
    public static void main(String[] args) {

        // Decrement operator

        int b = 10;
        System.out.println( --b + b--);
        System.out.println(b);

        System.out.println(--b + b++ + ++b);
        System.out.println(b);
    }


}
