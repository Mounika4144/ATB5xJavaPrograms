package src.basics_05_23Dec;

public class Lab039 {
    public static void main(String[] args) {

        // while and Do while

        int i =1;           //--> Init
        while(i<=10){       //-->cond
            System.out.println(i); // --> Code execute
            i++;                   // --> inc /Dec
        }

        int j=1;
        while(j<=10){
            System.out.println(j);
            j = j+1;
        }
    }
}
